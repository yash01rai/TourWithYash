const mongoose = require('mongoose');
const slugify = require('slugify');
const validator = require('validator');

// Incase of embedding
// const User = require('./userModel');

const tourSchema = new mongoose.Schema(
  {
    name: {
      // schema type options
      type: String,
      required: [true, 'A Tour must have a name'], // validator
      unique: true,
      trim: true,
      maxlength: [40, 'A name must have less than equal to 40 characters'],
      minlength: [7, 'A name must have greater than equal to 7 characters']
      // validate: [validator.isAlpha, 'Name must only contain characters']
    },
    slug: String,
    duration: {
      type: Number,
      required: [true, 'A tour must have a duration']
    },
    maxGroupSize: {
      type: Number,
      required: [true, 'A tour must have a group size']
    },
    difficulty: {
      type: String,
      required: [true, 'A tour must have a difficulty level defined'],
      enum: {
        values: ['easy', 'medium', 'difficult'],
        message: 'Difficulty is either: easy, medium, difficult'
      }
    },
    ratingsAverage: {
      type: Number,
      default: 4.5,
      min: [1, 'A tour must have atleast 1.0 rating'],
      max: [5, 'Highest rating is 5.0'],
      set: val => Math.round(val * 10) / 10 // 4.6666, 46.6666, 4.7
    },
    ratingsQuantity: {
      type: Number,
      default: 0
    },
    price: {
      type: Number,
      required: [true, 'A tour must have a price']
    },
    priceDiscount: {
      type: Number,
      validate: {
        validator: function(val) {
          return val < this.price;
        },
        message: 'Discount price ${VALUE} should be below regular price'
      }
    },
    summary: {
      type: String,
      trim: true
    },
    description: {
      type: String,
      trim: true,
      required: [true, 'A tour must have a description']
    },
    imageCover: {
      type: String,
      required: [true, 'A tour must have a cover image']
    },
    images: [String],
    createdAt: {
      type: Date,
      default: Date.now(),
      select: false
    },
    startDates: [Date],
    secretTour: {
      // schema type options
      type: Boolean,
      default: false
    },
    startLocation: {
      // embedded document
      // GeoJSON
      type: {
        type: String,
        default: 'Point',
        enum: ['Point']
      },
      coordinates: [Number],
      address: String,
      description: String
    },
    locations: [
      {
        type: {
          type: String,
          default: 'Point',
          enum: ['Point']
        },
        coordinates: [Number],
        address: String,
        description: String,
        day: Number
      }
    ],
    guides: [
      {
        type: mongoose.Schema.ObjectId,
        ref: 'User'
      }
    ]
  },
  // { virtuals: true },
  {
    toJSON: { virtuals: true },
    toObject: { virtuals: true }
  }
);

// Single field index
// 1 - ascending and -1 descending
// performance increase
tourSchema.index({ price: 1, ratingsAverage: -1 });
tourSchema.index({ slug: 1 });

tourSchema.index({ startLocation: '2dsphere' });

tourSchema.virtual('durationWeeks').get(function() {
  return this.duration / 7;
});

// Virtual populate
tourSchema.virtual('reviews', {
  ref: 'Review',
  localField: '_id',
  foreignField: 'tour'
});

// tourSchema.set('toObject', { virtuals: true });
// tourSchema.set('toJSON', { virtuals: true });
// DOCUMENT MIDDLEWARE: runs before .save() and .create()
tourSchema.pre('save', function(next) {
  this.slug = slugify(this.name, { lower: true });
  next();
});

